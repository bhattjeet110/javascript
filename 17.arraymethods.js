
let num = [0, 10, 20, 30, 40]
console.log(num) // print array
let b = num.toString() // convert in to string
console.log(b, typeof b)
let c = num.join('-') // to join my string
console.log(c, typeof c)
let d = num.pop() // poped (remove) last elements
console.log(num, d)
let e = num.push(100) // pushed (ADD) last elements
console.log(num, e)
let f = num.shift() // removed first elements
console.log(num, f)
let g = num.unshift(60) // add first elements
console.log(num, g)